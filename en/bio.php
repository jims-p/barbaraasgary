          <p>The old saying “You have to learn the rules in order to break them,” certainly applies to Westmount artist Barbara Asgary. While Asgary was classically trained in figurative art in Paris at Ecole Nationale Superieure des Beaux-Arts and the Ecole Montparnasse, she has opted for the New York School minimalist approach to her work.<br>Asgary, who came to Canada from her native Czech Republic when she was three years old, cites Kandinsky, Rauschenberg and Rothko as having had a major influence on her work. In, 1943, Mark Rothko wrote about Abstract Expressionism saying “We favour the simple expression of the complex thought.” Asgary successfully carries on this tradition in her work by using the least amount of elements to suggest a compound scenario as is clearly evident in her large-scale “Snow” series of paintings. ‘Snowfeel II (The Crowd)’ gives us a bird’s eye view of snow falling from a blue sky onto the ‘suggestion’ of people rushing through the streets. Asgary manages to convey this scene replete with the impression of snow blowing through the air with only pale blue and white canvas. Asgary, who views her work as a “window to my inner existence,” says she has “an excitement for colour” but sticks to predominantly pastel palette which provides a calming atmosphere to her work.<br>There is a spiritual component - “a nice place to rest your mind,” she says - in her work, which hangs in collections across Canada, as well as in France, Mexico, the U.S. and Australia. This is an exhibition that should not be missed.<br><br><i>Darlene Cousins, The Westmount Examiner, Montreal</i></p>
